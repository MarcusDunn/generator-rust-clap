//! Generation entry point. Phase 0 iteration 2: emit a buildable flat
//! clap CLI crate that makes real HTTP calls via reqwest. Inputs and
//! outputs travel as `serde_json::Value` for now — type fidelity for
//! params and full model emission are followups.

use forge_plugin_sdk::ir::{Body, HttpMethod, Ir, Operation, Parameter};
use forge_plugin_sdk::{GenerationOutput, OutputFile};

use crate::config::Config;
use crate::naming::{kebab_case, pascal_case, screaming_snake, snake_case};

pub fn all(ir: &Ir, cfg: &Config) -> GenerationOutput {
    let bin_name = bin_name(ir, cfg);
    let pkg_name = format!("{bin_name}-cli");

    GenerationOutput {
        files: vec![
            OutputFile::text("Cargo.toml", emit_cargo_toml(&pkg_name, &bin_name)),
            OutputFile::text("src/main.rs", emit_main_rs(ir, cfg, &bin_name)),
            OutputFile::text("src/client.rs", emit_client_rs(ir)),
            OutputFile::text("src/runtime.rs", emit_runtime_rs()),
            OutputFile::text("README.md", emit_readme(ir, &bin_name)),
        ],
        diagnostics: vec![],
    }
}

fn bin_name(ir: &Ir, cfg: &Config) -> String {
    if let Some(n) = cfg.name.as_deref().filter(|s| !s.is_empty()) {
        return kebab_case(n);
    }
    let title = ir.info.title.trim();
    if title.is_empty() {
        "api-cli".into()
    } else {
        kebab_case(title)
    }
}

fn default_base_url(ir: &Ir, cfg: &Config) -> String {
    if let Some(u) = cfg.base_url.as_deref().filter(|s| !s.is_empty()) {
        return u.to_string();
    }
    ir.servers
        .first()
        .map(|s| s.url.clone())
        .unwrap_or_else(|| "http://localhost".into())
}

fn env_prefix(bin_name: &str) -> String {
    screaming_snake(bin_name)
}

